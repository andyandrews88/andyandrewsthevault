// ============= Helper Functions =============

export function getFoodsByCategory(category: FoodCategory): FoodItem[] {
  return foodDatabase.filter(food => food.category === category);
}

export function searchFoods(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase();
  return foodDatabase.filter(food => 
    food.name.toLowerCase().includes(lowerQuery) ||
    food.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    food.notes?.toLowerCase().includes(lowerQuery)
  );
}

export function getFoodById(id: string): FoodItem | undefined {
  return foodDatabase.find(food => food.id === id);
}

export function getCategoryLabel(category: FoodCategory): string {
  const labels: Record<FoodCategory, string> = {
    lean_protein: 'Lean Proteins',
    whole_protein: 'Whole Proteins',
    dairy_vegetarian: 'Dairy & Vegetarian',
    carbohydrate: 'Carbohydrates',
    healthy_fat: 'Healthy Fats',
    vegetable: 'Vegetables',
    fruit: 'Fruits',
    supplement: 'Supplements',
  };
  return labels[category];
}

export function calculateFoodMacros(food: FoodItem, servings: number) {
  return {
    calories: Math.round(food.calories * servings),
    protein: Math.round(food.protein * servings),
    carbs: Math.round(food.carbs * servings),
    fats: Math.round(food.fats * servings),
    fiber: food.fiber ? Math.round(food.fiber * servings) : 0,
  };
}
